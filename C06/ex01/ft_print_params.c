#include <unistd.h>

void	ft_print_params(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		write (1, &str[i], 1);
		i++;
	}
	write (1, "\n", 1);
}

int	main(int argc, char **argv)
{
	int	i;

	(void) argc;
	i = 1;
	while (argv[i])
	{
		ft_print_params(argv[i]);
		i++;
	}
}
